package genericP;

/********************************************************/
/*         NSobject doubleオブジェクトの生成            */
/********************************************************/

public class NSobject{

  double a;

/***************************************************************************/

  public NSobject(){
    a = 0.0;
  }

/***************************************************************************/

  public NSobject(double ta){
    this.a = ta;
  }

/***************************************************************************/

  public void setNS(double ta){
    this.a = ta;
  }

/***************************************************************************/

  public double getNS(){
    return this.a;
  }

}
